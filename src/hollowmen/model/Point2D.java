package hollowmen.model;

/**
 * This interface represent a generic 2D point
 * @author pigio
 *
 */
public interface Point2D {

	/**
	 * This method give the horizontal coordinate value
	 * @return {@code double}
	 */
	public double getX();
	
	/**
	 * This method give the vertical coordinate value
	 * @return {@code double}
	 */
	public double getY();
	
}
